function writeLocale(lang, data) {
  const dir = path.join(base, lang);
  fs.mkdirSync(dir, { recursive: true });
  for (const [ns, content] of Object.entries(data)) {
    fs.writeFileSync(path.join(dir, `${ns}.json`), `${JSON.stringify(content, null, 2)}\n`);
  }
}


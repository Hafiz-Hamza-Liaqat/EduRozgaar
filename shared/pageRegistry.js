const pageById = new Map(PAGE_REGISTRY.map((p) => [p.id, p]));

export function getPageById(pageId) {
  return pageById.get(pageId) ?? null;
}

export function getPageByRoute(route) {
  return PAGE_REGISTRY.find((p) => p.route === route) ?? null;
}

export function getPagesByCategory(category) {
  return PAGE_REGISTRY.filter((p) => p.category === category);
}

export function getAdCapablePages() {
  return PAGE_REGISTRY.filter((p) => p.adCapable);
}

export function getPickablePages(options = {}) {
  const {
    includeProtected = false,
    includeAdmin = false,
    adCapableOnly = false,
  } = options;
  return PAGE_REGISTRY.filter((p) => {
    if (!includeAdmin && p.adminOnly) return false;
    if (!includeProtected && p.protected) return false;
    if (adCapableOnly && !p.adCapable) return false;
    if (p.externalPickable === false) return false;
    return Boolean(p.route) && p.route !== '*';
  });
}

export function getPageCategories(options = {}) {
  const pages = getPickablePages(options);
  return [...new Set(pages.map((p) => p.category))].sort();
}
